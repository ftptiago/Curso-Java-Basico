/* Classe utilizanda para demostrar o uso ddo operador Logico OU ( || )*/

public class OperadorLogicoou {
    
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c = false;

        System.out.println(a || b);
        System.out.println(b || c);
    }
}
